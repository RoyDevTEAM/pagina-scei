      <h2>Misión</h2>
                    <p>Promover el desarrollo científico y profesional de nuestros miembros a través de actividades académicas y científicas que generan, difunden y aplican conocimientos en ingeniería para formar profesionales competentes y comprometidos con el desarrollo sostenible de la sociedad. 
</p>
                    <h2>Visión</h2>
                    <p>Ser una organización líder en la generación y difusión del conocimiento en el área de ingeniería, reconocida por la calidad y relevancia de sus actividades y por su contribución al desarrollo sostenible del país y de la región. 
</p>
                    <h2>Objetivo general</h2>
                    <p>La Sociedad Científica de la UPDS tiene como objetivo la generación y difusión de conocimientos científicos en diversas áreas de ingeniería, promoviendo la autogestión, el autoaprendizaje y la ejecución de proyectos de investigación innovadores, así como la redacción de artículos científicos. 
</p>
                    <h2>Objetivos especificos</h2>
                    <p>Constituirse en un recurso universitario válido e importante para los estudiantes que deseen compartir y profundizar su formación académica en el ámbito de la investigación.</p> 

<p>Fomentar la participación de los estudiantes de la UPDS en actividades de investigación científica a través de la organización de eventos y actividades que promuevan el intercambio de conocimientos y experiencias entre los miembros de la sociedad científica.</p> 

<p>Impulsar y desarrollar actividades de investigación para contribuir al avance del conocimiento científico y tecnológico en distintas áreas de la ingeniería.</p>

<p>Promover el intercambio de experiencias y la cooperación entre estudiantes y la Sociedad Científica dentro de la UPDS, para que puedan colaborar en proyectos de investigación y promover la generación de conocimiento científico en la universidad.</p> 

<p> una base de registro de los proyectos de investigación científica, tecnológica y social, producción intelectual realizada al interior de la sociedad científica de la UPDS.  Desarrollar cursos especiales sobre aspectos éticos del investigador y de la investigación, del dialogo intra e intercultural, el compromiso social, la solidaridad y honestidad con los resultados producidos, así como el liderazgo local en el desarrollo de la investigación científica.
</p>
